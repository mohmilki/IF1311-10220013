#materi casting

data_int = 9

print ("data =", data_int, ",type =", type(data_int))

data_fl = float(data_int)

print ("data =", data_fl, ",type", type (data_fl))

#tipe data integer
print ("======INTEGER======")
data_integer = 10
print ("data =", data_integer, ",type =", type(data_integer))
data_float = float(data_integer)
data_string = str(data_integer)
data_bool = bool(data_integer)
print ("data =", data_float, ",type =", type(data_float))
print ("data =", data_string, ",type =", type(data_string))
print ("data =", data_bool, ",type =", type(data_bool))

#tipe data float
data_float = 10.5
print ("======FLOAT======")
print ("data =", data_float, ",type =", type(data_float))
data_integer = int(data_float)
data_string = str(data_float)
data_bool = bool(data_float)
print ("data = ", data_integer, ",type", type(data_integer))
print ("data =", data_string, ",type", type(data_string))
print ("data =", data_bool, ", type", type(data_bool))

#tipe data: biner true/flase (boolean)
data_bool = False

print ("======BOOLEAN======")
print ("data =", data_bool, ", type", type(data_bool))
data_integer = int(data_bool)
data_float = float(data_bool)
data_string = str(data_bool)
print ("data = ", data_integer, ",type", type(data_integer))
print ("data =", data_float, ",type", type(data_float))
print ("data =", data_string, ", type", type(data_string))

#tipe data string
data_string = "Moh"
print ("======STRING======")
print ("data =", data_string, ", type", type(data_string))
data_integer = int(data_string)
data_bool = bool(data_string)
data_float = float(data_string)
print ("data = ", data_integer, ",type", type(data_integer))
print ("data =", data_float, ",type", type(data_float))
print ("data =", data_bool, ", type", type(data_bool))